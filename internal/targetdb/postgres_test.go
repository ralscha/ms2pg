func TestRenderCreateViewNormalizesCommonTSQL(t *testing.T) {
	view := &catalog.View{
		Schema:     "reporting",
		Name:       "user_labels",
		Definition: "SET ANSI_NULLS ON\nGO\nSET QUOTED_IDENTIFIER ON\nGO\nCREATE VIEW [reporting].[user_labels] AS\nSELECT [u].[id], ISNULL([u].[city], N'unknown') AS [city_label], GETDATE() AS [generated_at]\nFROM [dbo].[users] AS [u]",
	}

	statement, err := renderCreateView(view)
	if err != nil {
		t.Fatalf("renderCreateView returned error: %v", err)
	}

	wants := []string{
		`CREATE OR REPLACE VIEW "reporting"."user_labels" AS`,
		`COALESCE("u"."city", 'unknown') AS "city_label"`,
		`CURRENT_TIMESTAMP AS "generated_at"`,
		`FROM "dbo"."users" AS "u"`,
	}
	for _, want := range wants {
		if !strings.Contains(statement, want) {
			t.Fatalf("statement %q does not contain %q", statement, want)
		}
	}
}

func TestRenderCreateViewNormalizesAdditionalTSQLFunctions(t *testing.T) {
	view := &catalog.View{
		Schema:     "reporting",
		Name:       "user_metrics",
		Definition: `CREATE VIEW [reporting].[user_metrics] AS SELECT LEN([u].[name]) AS [name_len], DATALENGTH([u].[name]) AS [name_bytes], GETUTCDATE() AS [generated_utc] FROM [dbo].[users] AS [u]`,
	}

	statement, err := renderCreateView(view)
	if err != nil {
		t.Fatalf("renderCreateView returned error: %v", err)
	}

	wants := []string{
		`LENGTH("u"."name") AS "name_len"`,
		`OCTET_LENGTH("u"."name") AS "name_bytes"`,
		`(CURRENT_TIMESTAMP AT TIME ZONE 'UTC') AS "generated_utc"`,
	}
	for _, want := range wants {
		if !strings.Contains(statement, want) {
			t.Fatalf("statement %q does not contain %q", statement, want)
		}
	}
}
