func TestRenderCreateTableIncludesIdentityDefaultsAndPrimaryKey(t *testing.T) {
	table := &catalog.Table{
		Schema: "dbo",
		Name:   "users",
		Columns: []*catalog.Column{
			{Name: "id", TargetType: "bigint", Identity: true},
			{Name: "email", TargetType: "text", Nullable: false},
			{Name: "external_id", TargetType: "uuid", Nullable: false, Default: "gen_random_uuid()"},
		},
		PrimaryKey: []string{"id"},
	}

	statement := renderCreateTable(table)

	checks := []string{
		`CREATE TABLE IF NOT EXISTS "dbo"."users"`,
		`"id" bigint GENERATED BY DEFAULT AS IDENTITY`,
		`"email" text NOT NULL`,
		`"external_id" uuid NOT NULL DEFAULT gen_random_uuid()`,
		`PRIMARY KEY ("id")`,
	}

	for _, check := range checks {
		if !strings.Contains(statement, check) {
			t.Fatalf("statement %q does not contain %q", statement, check)
		}
	}
}

func TestRenderCreateViewRejectsUnsupportedTSQL(t *testing.T) {
	view := &catalog.View{
		Schema:     "reporting",
		Name:       "latest_user",
		Definition: `CREATE VIEW reporting.latest_user AS SELECT TOP (1) id FROM dbo.users ORDER BY id DESC`,
	}

	_, err := renderCreateView(view)
	if !errors.Is(err, errUnsupportedViewDefinition) {
		t.Fatalf("renderCreateView error = %v, want unsupported view definition", err)
	}
	if err == nil || !strings.Contains(err.Error(), `TOP `) {
		t.Fatalf("renderCreateView error = %v, want message mentioning TOP", err)
	}
}

func TestRenderCreateIndexIncludesUniquenessAndColumns(t *testing.T) {
	table := &catalog.Table{Schema: "dbo", Name: "users"}
	index := &catalog.Index{Name: "idx_users_email", Unique: true, Columns: []string{"email", "city"}}

	statement := renderCreateIndex(table, index)
	want := `CREATE UNIQUE INDEX IF NOT EXISTS "idx_users_email" ON "dbo"."users" ("email", "city")`
	if statement != want {
		t.Fatalf("renderCreateIndex() = %q, want %q", statement, want)
	}
}

func TestRenderCreateIndexSupportsIncludeAndPredicate(t *testing.T) {
	table := &catalog.Table{Schema: "dbo", Name: "users"}
	index := &catalog.Index{
		Name:            "idx_users_name_filtered",
		Columns:         []string{"name"},
		IncludedColumns: []string{"city"},
		Predicate:       `[city] IS NOT NULL`,
	}

	statement := renderCreateIndex(table, index)
	want := `CREATE INDEX IF NOT EXISTS "idx_users_name_filtered" ON "dbo"."users" ("name") INCLUDE ("city") WHERE "city" IS NOT NULL`
	if statement != want {
		t.Fatalf("renderCreateIndex() = %q, want %q", statement, want)
	}
}

func TestRenderCreateForeignKeyIncludesActions(t *testing.T) {
	table := &catalog.Table{Schema: "sales", Name: "orders"}
	foreignKey := &catalog.ForeignKey{
		Name:              "fk_orders_user_id",
		Columns:           []string{"user_id"},
		ReferencedSchema:  "dbo",
		ReferencedTable:   "users",
		ReferencedColumns: []string{"id"},
		UpdateRule:        "NO ACTION",
		DeleteRule:        "CASCADE",
	}

	statement := renderCreateForeignKey(table, foreignKey)
	want := `ALTER TABLE "sales"."orders" ADD CONSTRAINT "fk_orders_user_id" FOREIGN KEY ("user_id") REFERENCES "dbo"."users" ("id") ON UPDATE NO ACTION ON DELETE CASCADE`
	if statement != want {
		t.Fatalf("renderCreateForeignKey() = %q, want %q", statement, want)
	}
}

